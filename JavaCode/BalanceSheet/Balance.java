package JavaCode.BalanceSheet;

public class Balance {
    double toGive;
    double toReceive;

    public double getToGive() {
        return toGive;
    }
    public void setToGive(double toGive) {
        this.toGive = toGive;
    }
    public double getToReceive() {
        return toReceive;
    }
    public void setToReceive(double toReceive) {
        this.toReceive = toReceive;
    }

}
